int	ft_count_if(char **tab, int (*f)(char*))
{
	int	len;
	int	res;
	int	i;

	len = 0;
	res = 0;
	i = 0;
	while (tab[len])
		len++;
	while (i < len)
	{
		if ((*f)(tab[i]))
			res++;
		i++;
	}
	return (res);
}
